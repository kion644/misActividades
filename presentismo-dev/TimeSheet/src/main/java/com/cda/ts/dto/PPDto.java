package com.cda.ts.dto;

public class PPDto {
	private String codigo;
	private String descripcion;
	private String usuario;
	private String horasTotales;
	private String liderProyecto;
	private String codigoFase;
	private String fase;
	private String descripcionFase;
	private String abreviatura;
	private String cliente;

	public PPDto() {
		super();
	}

	public String getCodigo() {
		return codigo;
	}

	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}

	public String getUsuario() {
		return usuario;
	}

	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}

	public String getHorasTotales() {
		return horasTotales;
	}

	public void setHorasTotales(String horasTotales) {
		this.horasTotales = horasTotales;
	}

	public String getDescripcion() {
		return descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}


	public String getLiderProyecto() {
		return liderProyecto;
	}

	public void setLiderProyecto(String liderProyecto) {
		this.liderProyecto = liderProyecto;
	}

	public String getCodigoFase() {
		return codigoFase;
	}

	public void setCodigoFase(String codigoFase) {
		this.codigoFase = codigoFase;
	}

	public String getDescripcionFase() {
		return descripcionFase;
	}

	public void setDescripcionFase(String descripcionFase) {
		this.descripcionFase = descripcionFase;
	}

	public String getFase() {
		return fase;
	}

	public void setFase(String fase) {
		this.fase = fase;
	}

	public String getAbreviatura() {
		return abreviatura;
	}

	public void setAbreviatura(String abreviatura) {
		this.abreviatura = abreviatura;
	}

	public String getCliente() {
		return cliente;
	}

	public void setCliente(String cliente) {
		this.cliente = cliente;
	}

	

}
